// PAN Client Manager — hub-side WebSocket server for pan-client connections.
//
// Clients connect to /ws/client?token=<token>&device_id=<id>
// Each client sends: register, heartbeat, command_result, shell_output, disconnect
// Hub sends: any command from /api/v1/client/command
//
// Usage:
//   import { startClientServer, sendToClient, getConnectedClients } from './client-manager.js';
//   startClientServer(httpServer);  // Call before startTerminalServer

import { WebSocketServer, WebSocket } from 'ws';
import { get, all, run, insert } from './db.js';
import crypto from 'crypto';
import { hostname } from 'os';
import { broadcastNotification } from './terminal-bridge.js';

// In-memory client registry: device_id → { ws, info }
const clients = new Map();

// Rate-limiter for denied devices — prevents rapid reconnect spam in logs
// Maps device_id → { lastLog: timestamp, count: number }
const deniedRateLimit = new Map();
const DENIED_LOG_INTERVAL_MS = 60_000; // only log once per minute per device

// ── Token store (in-memory + DB) ─────────────────────────────────────────────
// Invite tokens are generated by /api/v1/client/invite and consumed on register.
// Stored in settings table as JSON: key='client_invite_tokens' value='[{token,name,expires,used}]'
function getTokens() {
  try {
    const row = get("SELECT value FROM settings WHERE key = 'client_invite_tokens'");
    return row ? JSON.parse(row.value) : [];
  } catch { return []; }
}
function saveTokens(tokens) {
  const existing = get("SELECT id FROM settings WHERE key = 'client_invite_tokens'");
  if (existing) {
    run("UPDATE settings SET value = :v WHERE key = 'client_invite_tokens'", { ':v': JSON.stringify(tokens) });
  } else {
    run("INSERT INTO settings (key, value) VALUES ('client_invite_tokens', :v)", { ':v': JSON.stringify(tokens) });
  }
}

export function createInviteToken(name = 'new-device', ttlMs = 24 * 60 * 60 * 1000) {
  const token = 'pan-' + crypto.randomBytes(16).toString('hex');
  const now = Date.now();
  // Prune expired + used tokens on every creation to prevent pile-up
  const tokens = getTokens().filter(t => !t.used && t.expires > now);
  tokens.push({ token, name, created: now, expires: now + ttlMs, used: false });
  saveTokens(tokens);
  return token;
}

function validateToken(token) {
  const tokens = getTokens();
  const entry = tokens.find(t => t.token === token && !t.used && t.expires > Date.now());
  return !!entry;
}

// Dev mode: allow any token starting with 'dev-'
// 'local' sentinel: connections from the same LAN/Tailscale network discovered
// via UDP broadcast or Tailscale peer scan — no invite needed, but still goes
// through the pending-approval gate so the hub owner must approve them.
function isTokenValid(token) {
  if (!token) return false;
  if (token === 'local') return true;        // local network discovery bypass
  if (token.startsWith('dev-')) return true; // dev bypass
  return validateToken(token);
}

// Exported so install route can check tokens without the dev bypass
export function checkInviteToken(token) {
  if (!token) return false;
  if (token.startsWith('dev-')) return true;
  return validateToken(token);
}

